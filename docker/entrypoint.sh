#!/bin/sh
# Pre-flight setup before Apache starts.
# Handles DB initialization, migrations, schema validation, and cache warming
# so that `docker compose up -d` is the only command needed.

set -e

APP_DIR="/var/www/html/ibl5"
LOGS_DIR="$APP_DIR/logs"
DB_HOST="${DB_HOST:-mariadb}"
DB_NAME="iblhoops_ibl5"

# Wrapper to suppress MariaDB "password on command line" warning.
# --skip-ssl: Docker-internal connections don't use TLS.
db_exec() {
    mariadb -h "$DB_HOST" --skip-ssl -u root -proot "$DB_NAME" "$@" 2>&1 | grep -v '\[Warning\].*password' || true
}

# PHP CLI scripts (migrate, validate-schema) include config.php which reads
# $_SERVER['SERVER_NAME']. Set it here so CLI context has it.
export SERVER_NAME=localhost

# ── Phase 0: Worktree mount guard ──────────────────────────────────────────
# Worktrees symlink vendor/ to the main repo — that symlink is unresolvable
# inside a container. If vendor is a dangling symlink, this container was
# launched via `docker compose up` from a worktree directory instead of
# using `bin/wt-up`. Abort with a clear message.
if [ -L "$APP_DIR/vendor" ] && [ ! -e "$APP_DIR/vendor" ]; then
    echo ""
    echo "============================================================"
    echo "  FATAL: Dangling vendor symlink detected."
    echo ""
    echo "  This container was started from a worktree directory using"
    echo "  the main docker-compose.yml. Worktree Docker environments"
    echo "  must be started with bin/wt-up <name>, not docker compose."
    echo ""
    echo "  To fix:"
    echo "    1. Stop this container"
    echo "    2. Run: docker compose up -d   (from the main repo root)"
    echo "       OR:  bin/wt-up <worktree-name>"
    echo "============================================================"
    echo ""
    exit 1
fi

# ── Phase 1: Filesystem setup ───────────────────────────────────────────────
mkdir -p "$LOGS_DIR"
chown www-data:www-data "$LOGS_DIR"

# Materialize the native engine binary into the bind-mounted app dir. The image
# builds it to /opt/ibl5-bin/jsbsim (outside the mount); copy it into ibl5/bin so
# it's visible despite the mount shadowing image-built paths under ibl5/. Guarded
# so a PHP-only image (no engine stage) still boots.
if [ -f /opt/ibl5-bin/jsbsim ]; then
    cp /opt/ibl5-bin/jsbsim "$APP_DIR/bin/jsbsim" && chmod +x "$APP_DIR/bin/jsbsim"
    echo "[entrypoint] Installed native engine binary at ibl5/bin/jsbsim."
fi

# Auto-create mail config for Docker (Mailpit SMTP) if none exists.
MAIL_CONFIG="$APP_DIR/config/mail.config.php"
if [ ! -f "$MAIL_CONFIG" ]; then
    cat > "$MAIL_CONFIG" << 'MAILCONF'
<?php
// Auto-generated by Docker entrypoint — routes mail to Mailpit.
// View captured emails at http://localhost:8025
// Override by editing this file; it won't be regenerated.
return [
    'transport' => 'smtp',
    'smtp' => [
        'host' => 'ibl5-mailpit',
        'port' => 1025,
        'encryption' => '',
        'username' => '',
        'password' => '',
    ],
    'default_from_email' => 'noreply@iblhoops.net',
    'default_from_name' => 'IBL',
];
MAILCONF
    echo "[entrypoint] Created mail.config.php (Mailpit SMTP)."
fi

# ── Phase 2: DB status check (warn if empty, never auto-import) ───────────
TABLE_COUNT=$(db_exec -N -e "SELECT COUNT(*) FROM information_schema.tables WHERE table_schema='$DB_NAME';" | tr -d '[:space:]')

if ! echo "$TABLE_COUNT" | grep -qE '^[0-9]+$'; then
    echo "[entrypoint] ERROR: Could not query table count (got: $TABLE_COUNT). Check DB connection."
    echo "[entrypoint] Continuing without DB status check..."
    TABLE_COUNT="-1"
fi

if [ "$TABLE_COUNT" = "0" ]; then
    echo "[entrypoint] WARNING: Database is empty (0 tables)."
    echo "[entrypoint] Populate it with: bin/dev-up (main stack) or bin/db-sync-prod"
elif [ "$TABLE_COUNT" != "-1" ]; then
    CONFIG_ROWS=$(db_exec -N -e "SELECT COUNT(*) FROM nuke_config;" 2>/dev/null | tr -d '[:space:]')
    if [ "$CONFIG_ROWS" = "0" ]; then
        echo "[entrypoint] WARNING: $TABLE_COUNT tables exist but nuke_config is empty — data was lost."
        echo "[entrypoint] Re-populate with: bin/db-sync-prod"
    else
        echo "[entrypoint] Database has $TABLE_COUNT tables with data — ready."
    fi
fi

# ── Phase 3: Migrations (always, idempotent) ────────────────────────────────
echo "[entrypoint] Running pending migrations..."
php "$APP_DIR/bin/migrate" 2>&1 || echo "[entrypoint] WARNING: Migration runner failed (non-fatal)."

# ── Phase 4: Schema validation (warn-only) ──────────────────────────────────
echo "[entrypoint] Validating schema..."
php "$APP_DIR/bin/validate-schema" 2>&1 || echo "[entrypoint] WARNING: Schema validation failed. Check migrations."

# ── Phase 5: Vendor check ───────────────────────────────────────────────────
if [ ! -f "$APP_DIR/vendor/autoload.php" ]; then
    echo "[entrypoint] WARNING: vendor/autoload.php not found."
    echo "[entrypoint] Run 'composer install' on the host to install dependencies."
fi

# ── Phase 6: Purge stale page cache and warm caches ─────────────────────────
if [ -f "$APP_DIR/vendor/autoload.php" ]; then
    echo "[entrypoint] Purging page cache..."
    php -r "require '$APP_DIR/vendor/autoload.php'; Cache\PageCache::purge();" 2>/dev/null || true
    echo "[entrypoint] Warming caches in background..."
    (php "$APP_DIR/bin/warm-cache" >> "$LOGS_DIR/warm-cache.log" 2>&1) &
fi

# ── Phase 7: Start Apache ───────────────────────────────────────────────────
echo "[entrypoint] Starting Apache."
exec "$@"
