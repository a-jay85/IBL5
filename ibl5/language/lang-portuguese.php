{
    switch ($phrase) {
        case "xdatestring":$tmp = "%A, %B %d @ %T %Z";
            break;
        case "linksdatestring":$tmp = "%d-%b-%Y";
            break;
        case "xdatestring2":$tmp = "%A, %B %d";
            break;
        default:$tmp = "$phrase";
            break;
    }
    return $tmp;
}
